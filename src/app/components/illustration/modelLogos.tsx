interface Family {
  label: string;
  /** Disc background. */
  bg: string;
  /** Disc border. */
  ring: string;
  Logo: () => ReactNode;
}

/** Order around the ring; each appears `RING_COPIES` times, evenly spaced. */
const FAMILY_ORDER: Family[] = [
  { label: "Mistral", bg: "#FFFFFF", ring: "rgba(8,15,10,0.12)", Logo: MistralMark },
  { label: "Meta", bg: "#FFFFFF", ring: "rgba(8,15,10,0.12)", Logo: MetaMark },
  { label: "Qwen", bg: "#FFFFFF", ring: "rgba(8,15,10,0.12)", Logo: QwenMark },
  { label: "DeepSeek", bg: "#FFFFFF", ring: "rgba(8,15,10,0.12)", Logo: DeepSeekMark },
  { label: "Gemma", bg: "#FFFFFF", ring: "rgba(8,15,10,0.12)", Logo: GemmaMark },
  { label: "HuggingFace", bg: "#FFFFFF", ring: "rgba(8,15,10,0.12)", Logo: HuggingFaceMark },
];

const RING_COPIES = 1;
/** Polar angle of the ring from the front pole (−z), in radians. */
const RING_ALPHA = (58 * Math.PI) / 180;

export interface BubbleSeed extends Family {
  /** Unit-length seed position on the sphere. */
  pos: Vec3;
  /** Index of this bubble around the even circle (0..count-1). */
  ringIndex: number;
}

/**
 * Bubbles seeded evenly in azimuth. They live on a fixed screen-plane ring in
 * `PointCloud` (positioned by `ringIndex`); `pos` is kept for any 3D fallback.
 */
function buildSeeds(): BubbleSeed[] {
  const seeds: BubbleSeed[] = [];
  const total = FAMILY_ORDER.length * RING_COPIES;
  const sinA = Math.sin(RING_ALPHA);
  const cosA = Math.cos(RING_ALPHA);
  for (let c = 0; c < RING_COPIES; c++) {
    for (let f = 0; f < FAMILY_ORDER.length; f++) {
      const ringIndex = c * FAMILY_ORDER.length + f;
      const theta = -Math.PI / 2 + (2 * Math.PI * ringIndex) / total;
      seeds.push({
        ...FAMILY_ORDER[f],
        ringIndex,
        pos: { x: sinA * Math.cos(theta), y: sinA * Math.sin(theta), z: -cosA },
      });
    }
  }
  return seeds;
}

export const BUBBLE_SEEDS: BubbleSeed[] = buildSeeds();
export const BUBBLE_COUNT = BUBBLE_SEEDS.length;
